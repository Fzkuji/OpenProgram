def slugify(text: str) -> str:
    """Convert free text to a filesystem-safe wiki slug."""
    s = re.sub(r"[^a-zA-Z0-9\-_]+", "-", text.strip().lower())
    s = re.sub(r"-+", "-", s).strip("-")
    return s or "untitled"
